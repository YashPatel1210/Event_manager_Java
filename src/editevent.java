    public ArrayList<eventbean> retriveData() {
    
        ArrayList<eventbean> e=null;
        e=new ArrayList<eventbean>(); 
        try{
            Connection conn=MySqlconnection();
            String q="select * from event";
            Statement st=conn.createStatement();
            ResultSet rs=st.executeQuery(q);
            eventbean event;
            while(rs.next()){
                
                byte[] y=new byte[1024];
                y=rs.getBytes(5);
                event=new eventbean(rs.getString("name"),rs.getString("date"),rs.getString("time"),rs.getString("venue"),rs.getFloat(5),y);
                e.add(event);
            }
            
        }catch(Exception p){
            
            System.out.println("Error in retrieving data  :"+p);
        }
            
        
    return e;
}
    
    
    public void filltable(){
        ArrayList<eventbean> al=retriveData();
        DefaultTableModel dtm=(DefaultTableModel)edit_table.getModel();
        dtm.setRowCount(0);
        Object[] row=new Object[6];
        for(int i=0;i<al.size();i++){
            row[0]=al.get(i).getName();
            row[1]=al.get(i).getDate();
            row[3]=al.get(i).getVenue();
            row[4]=al.get(i).getFees();
            row[2]=al.get(i).getTime();
           
            dtm.addRow(row);
        }
    }
    
    public void showitem(int index){
        edit_eventname.setText(retriveData().get(index).getName());
        edit_venue.setText(retriveData().get(index).getVenue());
        edit_fees.setText(Float.toString(retriveData().get(index).getFees()));
        edit_time.setText(retriveData().get(index).getTime());
                
        try{
            
            java.util.Date date=null;
            date=new SimpleDateFormat("dd-MM-yyy").parse((String)retriveData().get(index).getDate());
            
            edit_date.setDate(date);
            
         
            
        }catch(Exception e){
            System.out.println(e);
        }
        
        showphoto.setIcon(resetImageSize(null,retriveData().get(index).getPhoto()));
    }
    
    

    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
       
       if (edit_eventname !=null || edit_date!=null
                    || edit_fees !=null || edit_venue!=null) {
           
           String q=null;
           PreparedStatement ps=null;
           Connection conn=MySqlconnection();
           if(photopath==null){
           try{
               q="update event set date=?, venue=?, fees=? ,time=? WHERE name=? ";
               ps=conn.prepareStatement(q);
               
               SimpleDateFormat sdf=new SimpleDateFormat("dd-MM-yyyy");
               String date1=sdf.format(edit_date.getDate());
               ps.setString(1,date1 );
               ps.setString(4, edit_time.getText());
               ps.setString(2,edit_venue.getText());
               
               ps.setFloat(3, Float.parseFloat(edit_fees.getText()));
               ps.setString(5, edit_eventname.getText());
               int res=ps.executeUpdate();
               if(res>=1){ 
                        JOptionPane.showMessageDialog(null,"Event Updated....");
                        filltable();
               }else{
                   JOptionPane.showMessageDialog(null,"Failed to Updated Event");
               }
               
           }catch(Exception e){
               JOptionPane.showMessageDialog(null,e);
           }
       }else{
               try{
               q="update event set date=?, venue=?, fees=? ,photo=?,time=? WHERE name=? ";
               ps=conn.prepareStatement(q);
               
               SimpleDateFormat sdf=new SimpleDateFormat("dd-MM-yyyy");
               String date1=sdf.format(edit_date.getDate());
               ps.setString(1,date1 );
               ps.setString(5,edit_time.getText());
               ps.setString(2,edit_venue.getText());
               
               ps.setFloat(3, Float.parseFloat(edit_fees.getText()));
       
               InputStream is=new FileInputStream(new File(photopath));
               ps.setBlob(4, is);
               
               ps.setString(6, edit_eventname.getText());
               int res=ps.executeUpdate();
               if(res>=1){ 
                        JOptionPane.showMessageDialog(null,"Event Updated....");
                        filltable();
               }else{
                   JOptionPane.showMessageDialog(null,"Failed to Updated Event");
               }
               
           }catch(Exception e){
               JOptionPane.showMessageDialog(null,e);
           }
           }
       }              
        
        
        
        
        
    }//GEN-LAST:event_updateActionPerformed

